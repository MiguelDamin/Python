"""Escreva um algoritmo para ler 2 valores (considere que não serão lidos
valores iguais) e escrevê-los em ordem crescente."""

valor1 = input("Valor 1: ")
valor2 = input("Valor 2: ")

if valor1<valor2:
    print(valor1)
else:
    print(valor2)
if valor1>valor2:
    print(valor1)
else:
    print(valor2)
